//! Support Tickets Integrity Zome
//! Entry types and validation for community support ticket management.
//!
//! Manages support tickets, comments, autonomous actions, undo operations,
//! and preemptive alerts for Symthaea-assisted community support.

use hdi::prelude::*;
use support_types::{
    ActionType as SupportActionType, AutonomyLevel, EpistemicStatus, SupportCategory,
    TicketPriority, TicketStatus,
};

// ============================================================================
// ANCHOR
// ============================================================================

/// Anchor entry for deterministic link bases
#[hdk_entry_helper]
#[derive(Clone, PartialEq)]
pub struct Anchor(pub String);

// ============================================================================
// SUPPORT TICKET
// ============================================================================

/// A community support ticket with optional Symthaea autonomy
#[hdk_entry_helper]
#[derive(Clone, PartialEq)]
pub struct SupportTicket {
    pub title: String,
    pub description: String,
    pub category: SupportCategory,
    pub priority: TicketPriority,
    pub status: TicketStatus,
    pub requester: AgentPubKey,
    pub assignee: Option<AgentPubKey>,
    pub autonomy_level: AutonomyLevel,
    pub system_info: Option<String>,
    pub is_preemptive: bool,
    pub prediction_confidence: Option<f32>,
    pub created_at: Timestamp,
    pub updated_at: Timestamp,
}

// ============================================================================
// TICKET COMMENT
// ============================================================================

/// A comment on a support ticket (human or Symthaea-generated)
#[hdk_entry_helper]
#[derive(Clone, PartialEq)]
pub struct TicketComment {
    pub ticket_hash: ActionHash,
    pub author: AgentPubKey,
    pub content: String,
    pub is_symthaea_response: bool,
    pub confidence: Option<f32>,
    pub epistemic_status: Option<EpistemicStatus>,
}

// ============================================================================
// AUTONOMOUS ACTION
// ============================================================================

/// An autonomous action proposed or taken by Symthaea on a ticket
#[hdk_entry_helper]
#[derive(Clone, PartialEq)]
pub struct AutonomousAction {
    pub ticket_hash: ActionHash,
    pub action_type: SupportActionType,
    pub description: String,
    pub approved: bool,
    pub executed: bool,
    pub result: Option<String>,
    pub success: Option<bool>,
    pub rollback_steps: Option<Vec<String>>,
    pub rollback_state: Option<String>,
    pub rolled_back: bool,
    pub created_at: Timestamp,
}

// ============================================================================
// UNDO ACTION
// ============================================================================

/// Record of an undone autonomous action with rollback result
#[hdk_entry_helper]
#[derive(Clone, PartialEq)]
pub struct UndoAction {
    pub original_action_hash: ActionHash,
    pub reason: String,
    pub rollback_result: String,
    pub created_at: Timestamp,
}

// ============================================================================
// PREEMPTIVE ALERT
// ============================================================================

/// A preemptive alert generated by Symthaea's predictive subsystem
#[hdk_entry_helper]
#[derive(Clone, PartialEq)]
pub struct PreemptiveAlert {
    pub predicted_failure: String,
    pub expected_time_to_failure: Option<String>,
    pub free_energy: f64,
    pub recommended_action: String,
    pub auto_generated_ticket: Option<ActionHash>,
    pub created_at: Timestamp,
}

