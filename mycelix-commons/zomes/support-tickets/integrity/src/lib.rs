    fn validate_link_tag(link_type: &LinkTypes, tag_len: usize) -> ValidateCallbackResult {
        let tag = LinkTag(vec![0u8; tag_len]);
        let max = 256;
        let name = match link_type {
            LinkTypes::ShardedTickets => "ShardedTickets",
            LinkTypes::AgentToTicket => "AgentToTicket",
            LinkTypes::StatusToTicket => "StatusToTicket",
            LinkTypes::CategoryToTicket => "CategoryToTicket",
            LinkTypes::TicketToComment => "TicketToComment",
            LinkTypes::TicketToAction => "TicketToAction",
            LinkTypes::ActionToUndo => "ActionToUndo",
            LinkTypes::AgentToAlert => "AgentToAlert",
            LinkTypes::TicketToEscalations => "TicketToEscalations",
            LinkTypes::TicketToSurvey => "TicketToSurvey",
        };
        if tag.0.len() > max {
            ValidateCallbackResult::Invalid(format!(
                "{} link tag too long (max {} bytes)",
                name, max
            ))
        } else {
            ValidateCallbackResult::Valid
        }
    }

