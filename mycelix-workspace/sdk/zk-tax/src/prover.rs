//! ZK proof generation for tax bracket membership.
//!
//! # Security Notice
//!
//! Production builds MUST use the `prover` feature for cryptographic guarantees.
//! Dev mode / simulation is ONLY for testing and provides NO security.
//!
//! # Usage
//!
//! ## Production (with `prover` feature):
//! ```rust,ignore
//! use mycelix_zk_tax::{ProductionTaxProver, Jurisdiction, FilingStatus};
//!
//! let prover = ProductionTaxProver::new()?;
//! let proof = prover.prove(85_000, Jurisdiction::US, FilingStatus::Single, 2024)?;
//! // Proof contains real RISC-0 cryptographic proof
//! ```
//!
//! ## Testing (with simulation):
//! ```rust,ignore
//! use mycelix_zk_tax::{SimulationTaxProver, Jurisdiction, FilingStatus};
//!
//! // WARNING: Simulation proofs have NO cryptographic guarantees!
//! let prover = SimulationTaxProver::new_for_testing();
//! let proof = prover.prove(85_000, Jurisdiction::US, FilingStatus::Single, 2024)?;
//! assert!(proof.is_simulation_proof()); // Always true for simulation
//! ```

