/// Production-grade tax bracket prover using RISC-0.
///
/// This prover generates real cryptographic proofs that provide
/// actual security guarantees. Requires the `prover` feature.
#[cfg(feature = "prover")]
pub struct ProductionTaxProver;

#[cfg(feature = "prover")]
impl ProductionTaxProver {
    /// Create a new production prover
    pub fn new() -> Result<Self> {
        Ok(Self)
    }

    /// Generate a real ZK proof of tax bracket membership
    pub fn prove(
        &self,
        income: u64,
        jurisdiction: Jurisdiction,
        filing_status: FilingStatus,
        tax_year: TaxYear,
    ) -> Result<TaxBracketProof> {
        let legacy_prover = TaxBracketProver::new();
        legacy_prover.prove(income, jurisdiction, filing_status, tax_year)
    }
}

