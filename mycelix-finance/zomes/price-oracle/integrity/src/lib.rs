#[hdk_extern]
pub fn genesis_self_check(_data: GenesisSelfCheckData) -> ExternResult<ValidateCallbackResult> {
    Ok(ValidateCallbackResult::Valid)
}

#[hdk_extern]
pub fn validate(op: Op) -> ExternResult<ValidateCallbackResult> {
    match op.flattened::<EntryTypes, LinkTypes>()? {
        FlatOp::StoreEntry(store_entry) => match store_entry {
            OpEntry::CreateEntry { app_entry, .. } => match app_entry {
                EntryTypes::PriceReport(report) => validate_price_report(&report),
                EntryTypes::PriceConsensus(consensus) => validate_consensus(&consensus),
                EntryTypes::BasketDefinition(basket) => validate_basket(&basket),
                EntryTypes::VolatilityAlert(_) => Ok(ValidateCallbackResult::Valid),
                EntryTypes::ReporterAccuracy(acc) => validate_reporter_accuracy(&acc),
                EntryTypes::Anchor(_) => Ok(ValidateCallbackResult::Valid),
            },
            OpEntry::UpdateEntry { app_entry, .. } => match app_entry {
                // Price reports are immutable once submitted
                EntryTypes::PriceReport(_) => Ok(ValidateCallbackResult::Invalid(
                    "Price reports cannot be updated — submit a new report instead".into(),
                )),
                // Consensus entries are replaced, not updated
                EntryTypes::PriceConsensus(consensus) => validate_consensus(&consensus),
                // Baskets can be updated by creator
                EntryTypes::BasketDefinition(basket) => validate_basket(&basket),
                EntryTypes::VolatilityAlert(_) => Ok(ValidateCallbackResult::Valid),
                // Accuracy is mutable — updated after each consensus round
                EntryTypes::ReporterAccuracy(acc) => validate_reporter_accuracy(&acc),
                EntryTypes::Anchor(_) => Ok(ValidateCallbackResult::Invalid(
                    "Anchors cannot be updated".into(),
                )),
            },
            _ => Ok(ValidateCallbackResult::Valid),
        },
        FlatOp::RegisterCreateLink { .. } => Ok(ValidateCallbackResult::Valid),
        FlatOp::RegisterDeleteLink { .. } => Ok(ValidateCallbackResult::Valid),
        FlatOp::StoreRecord(_) => Ok(ValidateCallbackResult::Valid),
        FlatOp::RegisterAgentActivity(_) => Ok(ValidateCallbackResult::Valid),
        FlatOp::RegisterUpdate(_) => Ok(ValidateCallbackResult::Valid),
        FlatOp::RegisterDelete(_) => Ok(ValidateCallbackResult::Valid),
    }
}

fn validate_price_report(report: &PriceReport) -> ExternResult<ValidateCallbackResult> {
    if report.item.is_empty() || report.item.len() > MAX_ITEM_NAME_LEN {
        return Ok(ValidateCallbackResult::Invalid(
            format!("Item name must be 1-{MAX_ITEM_NAME_LEN} characters"),
        ));
    }
    if !report.price_tend.is_finite() || report.price_tend <= 0.0 {
        return Ok(ValidateCallbackResult::Invalid(
            "Price must be a positive finite number".into(),
        ));
    }
    if report.evidence.len() > MAX_EVIDENCE_LEN {
        return Ok(ValidateCallbackResult::Invalid(
            format!("Evidence must be <= {MAX_EVIDENCE_LEN} characters"),
        ));
    }
    if report.reporter_did.is_empty() || report.reporter_did.len() > MAX_DID_LEN {
        return Ok(ValidateCallbackResult::Invalid(
            "Reporter DID is invalid".into(),
        ));
    }
    Ok(ValidateCallbackResult::Valid)
}

fn validate_consensus(consensus: &PriceConsensus) -> ExternResult<ValidateCallbackResult> {
    if consensus.item.is_empty() || consensus.item.len() > MAX_ITEM_NAME_LEN {
        return Ok(ValidateCallbackResult::Invalid(
            "Item name is invalid".into(),
        ));
    }
    if !consensus.median_price.is_finite() || consensus.median_price <= 0.0 {
        return Ok(ValidateCallbackResult::Invalid(
            "Median price must be positive and finite".into(),
        ));
    }
    if consensus.reporter_count == 0 {
        return Ok(ValidateCallbackResult::Invalid(
            "Consensus requires at least 1 reporter".into(),
        ));
    }
    Ok(ValidateCallbackResult::Valid)
}

fn validate_basket(basket: &BasketDefinition) -> ExternResult<ValidateCallbackResult> {
    if basket.name.is_empty() || basket.name.len() > MAX_BASKET_NAME_LEN {
        return Ok(ValidateCallbackResult::Invalid(
            format!("Basket name must be 1-{MAX_BASKET_NAME_LEN} characters"),
        ));
    }
    if basket.items.is_empty() || basket.items.len() > MAX_BASKET_ITEMS {
        return Ok(ValidateCallbackResult::Invalid(
            format!("Basket must have 1-{MAX_BASKET_ITEMS} items"),
        ));
    }
    let total_weight: f64 = basket.items.iter().map(|i| i.weight).sum();
    if !total_weight.is_finite() || (total_weight - 1.0).abs() > 0.05 {
        return Ok(ValidateCallbackResult::Invalid(
            "Basket weights must sum to approximately 1.0 (±0.05)".into(),
        ));
    }
    for item in &basket.items {
        if item.item.is_empty() || item.item.len() > MAX_ITEM_NAME_LEN {
            return Ok(ValidateCallbackResult::Invalid(
                "Basket item name is invalid".into(),
            ));
        }
        if !item.weight.is_finite() || item.weight <= 0.0 || item.weight > 1.0 {
            return Ok(ValidateCallbackResult::Invalid(
                "Basket item weight must be in (0.0, 1.0]".into(),
            ));
        }
    }
    if basket.creator_did.is_empty() || basket.creator_did.len() > MAX_DID_LEN {
        return Ok(ValidateCallbackResult::Invalid(
            "Creator DID is invalid".into(),
        ));
    }
    Ok(ValidateCallbackResult::Valid)
}

fn validate_reporter_accuracy(acc: &ReporterAccuracy) -> ExternResult<ValidateCallbackResult> {
    if acc.reporter_did.is_empty() || acc.reporter_did.len() > MAX_DID_LEN {
        return Ok(ValidateCallbackResult::Invalid(
            "Reporter DID is invalid".into(),
        ));
    }
    if !acc.accuracy_score.is_finite() || acc.accuracy_score < 0.0 || acc.accuracy_score > 1.0 {
        return Ok(ValidateCallbackResult::Invalid(
            "Accuracy score must be a finite number in [0.0, 1.0]".into(),
        ));
    }
    Ok(ValidateCallbackResult::Valid)
}
