def create_project_structure(project_path: Path, project_name: str) -> Tree:
    """Create the project directory structure and return a tree visualization."""
    tree = Tree(f"[bold cyan]{project_name}/[/bold cyan]")

    directories = [
        "config",
        "config/grafana/provisioning/dashboards",
        "config/grafana/provisioning/datasources",
        "src",
        "data",
        "logs",
        "metrics",
        "tests",
        "scripts",
    ]

    for dir_path in directories:
        (project_path / dir_path).mkdir(parents=True, exist_ok=True)

    # Add to tree
    tree.add("[dim]config/[/dim]")
    tree.add("[dim]src/[/dim]")
    tree.add("[dim]data/[/dim]")
    tree.add("[dim]logs/[/dim]")
    tree.add("[dim]metrics/[/dim]")
    tree.add("[dim]tests/[/dim]")
    tree.add("[dim]scripts/[/dim]")

    return tree


def write_template_file(path: Path, content: str) -> None:
    """Write a template file."""
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w") as f:
        f.write(content)


@app.callback(invoke_without_command=True)
def init_main(
    ctx: typer.Context,
    project_name: str = typer.Argument(
        None,
        help="Name of the project directory to create",
    ),
    path: Optional[Path] = typer.Option(
        None,
        "--path",
        "-p",
        help="Parent directory for the project (default: current directory)",
    ),
    docker: bool = typer.Option(
        True,
        "--docker/--no-docker",
        help="Include Docker Compose setup",
    ),
    example: bool = typer.Option(
        True,
        "--example/--no-example",
        help="Include example node code",
    ),
    monitoring: bool = typer.Option(
        True,
        "--monitoring/--no-monitoring",
        help="Include Prometheus/Grafana monitoring",
    ),
    force: bool = typer.Option(
        False,
        "--force",
        "-f",
        help="Overwrite existing files",
    ),
):
    """
    Initialize a new Mycelix FL project.

    Creates a project directory with configuration files,
    Docker Compose setup, and example code.

    [bold]Examples:[/bold]

        mycelix init my-fl-project
        mycelix init my-project --no-docker
        mycelix init healthcare-fl --path /projects
    """
    if ctx.invoked_subcommand is not None:
        return

    # Interactive mode if no project name
    if project_name is None:
        console.print("\n[bold cyan]Mycelix Project Initializer[/bold cyan]\n")
        project_name = Prompt.ask("Project name", default="my-mycelix-project")
        docker = Confirm.ask("Include Docker Compose setup?", default=True)
        example = Confirm.ask("Include example node code?", default=True)
        monitoring = Confirm.ask("Include monitoring (Prometheus/Grafana)?", default=True)

    # Determine project path
    base_path = path or Path.cwd()
    project_path = base_path / project_name

    # Check if exists
    if project_path.exists() and not force:
        console.print(f"[red]Error:[/red] Directory '{project_path}' already exists.")
        console.print("[dim]Use --force to overwrite.[/dim]")
        raise typer.Exit(1)

    console.print(f"\n[bold cyan]Creating Mycelix project: {project_name}[/bold cyan]\n")

    with Progress(
        SpinnerColumn(),
        TextColumn("[progress.description]{task.description}"),
        console=console,
    ) as progress:
        # Create directory structure
        task = progress.add_task("Creating directory structure...", total=None)
        tree = create_project_structure(project_path, project_name)
        progress.update(task, completed=True)

        # Write config files
        task = progress.add_task("Writing configuration files...", total=None)
        write_template_file(project_path / "config" / "mycelix.yaml", CONFIG_TEMPLATE)
        write_template_file(project_path / "requirements.txt", REQUIREMENTS_TEMPLATE)
        write_template_file(project_path / ".gitignore", GITIGNORE_TEMPLATE)
        progress.update(task, completed=True)

        # Write Docker files
        if docker:
            task = progress.add_task("Setting up Docker Compose...", total=None)
            write_template_file(project_path / "docker-compose.yml", DOCKER_COMPOSE_TEMPLATE)
            write_template_file(project_path / "Dockerfile.node", DOCKERFILE_NODE_TEMPLATE)
            write_template_file(project_path / "Dockerfile.coordinator", DOCKERFILE_COORDINATOR_TEMPLATE)
            progress.update(task, completed=True)

        # Write example code
        if example:
            task = progress.add_task("Creating example node code...", total=None)
            write_template_file(project_path / "src" / "node.py", EXAMPLE_NODE_TEMPLATE)
            write_template_file(project_path / "src" / "__init__.py", '"""Mycelix FL Project"""')
            progress.update(task, completed=True)

        # Write monitoring config
        if monitoring:
            task = progress.add_task("Configuring monitoring...", total=None)
            write_template_file(project_path / "config" / "prometheus.yml", PROMETHEUS_CONFIG_TEMPLATE)
            progress.update(task, completed=True)

    # Show success message
    console.print("\n[bold green]Project created successfully![/bold green]\n")

    # Show project structure
    tree = Tree(f"[bold cyan]{project_name}/[/bold cyan]")
    config_branch = tree.add("config/")
    config_branch.add("mycelix.yaml")
    if monitoring:
        config_branch.add("prometheus.yml")
    src_branch = tree.add("src/")
    if example:
        src_branch.add("node.py")
    src_branch.add("__init__.py")
    tree.add("[dim]data/[/dim]")
    tree.add("[dim]logs/[/dim]")
    tree.add("[dim]metrics/[/dim]")
    tree.add("[dim]tests/[/dim]")
    tree.add("requirements.txt")
    tree.add(".gitignore")
    if docker:
        tree.add("docker-compose.yml")
        tree.add("Dockerfile.node")
        tree.add("Dockerfile.coordinator")

    console.print(tree)
    console.print()

    # Next steps
    next_steps = f"""
[bold]Next Steps:[/bold]

1. [cyan]cd {project_name}[/cyan]
   Enter the project directory

2. [cyan]pip install -r requirements.txt[/cyan]
   Install Python dependencies

3. [cyan]Edit config/mycelix.yaml[/cyan]
   Configure your FL network

4. [cyan]Edit src/node.py[/cyan]
   Implement your model and training logic
"""

    if docker:
        next_steps += """
5. [cyan]docker-compose up -d[/cyan]
   Start the FL network with Docker
"""

    next_steps += """
[dim]Run 'mycelix demo' to see a working example.[/dim]
"""

    console.print(Panel(next_steps, title="Getting Started", border_style="green"))


@app.command()
def config():
    """Generate a configuration file interactively."""
    console.print("\n[bold cyan]Configuration Generator[/bold cyan]\n")

    # Gather configuration options
    num_nodes = int(Prompt.ask("Number of FL nodes", default="10"))
    byzantine_detection = Confirm.ask("Enable Byzantine detection?", default=True)
    compression = Confirm.ask("Enable HyperFeel compression?", default=True)
    dp_enabled = Confirm.ask("Enable Differential Privacy?", default=True)
    output_path = Prompt.ask("Output path", default="mycelix.yaml")

    # Generate config
    config = f"""# Mycelix FL Configuration
# Generated interactively by: mycelix init config

network:
  num_nodes: {num_nodes}
  bootstrap_nodes:
    - "ws://localhost:8080"

training:
  rounds: 100
  batch_size: 32
  learning_rate: 0.01

byzantine:
  enabled: {str(byzantine_detection).lower()}
  detection_threshold: 0.7

compression:
  enabled: {str(compression).lower()}
  method: hyperfeel
  target_ratio: 1000

privacy:
  differential_privacy:
    enabled: {str(dp_enabled).lower()}
    epsilon: 1.0
"""

    # Write config
    with open(output_path, "w") as f:
        f.write(config)

    console.print(f"\n[green]Configuration saved to {output_path}[/green]")


if __name__ == "__main__":
    app()
