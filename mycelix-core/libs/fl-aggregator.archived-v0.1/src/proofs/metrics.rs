//! Prometheus Metrics for Proof Operations
//!
//! Comprehensive metrics collection for monitoring zkSTARK proof generation
//! and verification performance.
//!
//! ## Metrics Overview
//!
//! | Metric | Type | Description |
//! |--------|------|-------------|
//! | `proof_generation_total` | Counter | Total proofs generated |
//! | `proof_generation_duration_seconds` | Histogram | Proof generation time |
//! | `proof_verification_total` | Counter | Total proofs verified |
//! | `proof_verification_duration_seconds` | Histogram | Verification time |
//! | `proof_size_bytes` | Histogram | Proof sizes in bytes |
//! | `batch_verification_total` | Counter | Total batch verifications |
//! | `batch_verification_size` | Histogram | Batch sizes |
//! | `gpu_operations_total` | Counter | GPU operations performed |
//! | `cache_hits_total` | Counter | Cache hits |
//! | `cache_misses_total` | Counter | Cache misses |
//!
//! ## Usage
//!
//! ```rust,ignore
//! use fl_aggregator::proofs::metrics::{ProofMetrics, METRICS};
//!
//! // Record a proof generation
//! METRICS.record_generation(ProofType::Range, 0.05, 15000);
//!
//! // Record a verification
//! METRICS.record_verification(ProofType::Range, true, 0.01);
//!
//! // Get metrics for Prometheus scraping
//! let output = METRICS.gather();
//! ```
//!
//! ## Integration with HTTP Server
//!
//! ```rust,ignore
//! use axum::{Router, routing::get};
//! use fl_aggregator::proofs::metrics::METRICS;
//!
//! async fn metrics_handler() -> String {
//!     METRICS.gather()
//! }
//!
//! let app = Router::new().route("/metrics", get(metrics_handler));
//! ```

