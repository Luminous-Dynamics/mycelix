//
// This enum represents the verification state of a PoGQ proof, distinguishing
// between what can be verified on-chain (in WASM) and what requires external
// verification (full RISC Zero STARK proof).

/// Verification status for PoGQ proofs
///
/// Represents the level of verification achieved for a proof entry.
/// Full ZK verification is not possible in WASM, so proofs can only
/// reach `PartiallyVerified` status on-chain.
///
/// ## Status Meanings
///
/// - `FullyVerified`: All checks passed including full STARK verification.
///   **Note**: This status is NOT achievable in WASM - it can only be set
///   after external verification by a native RISC Zero verifier.
///
/// - `PartiallyVerified`: Receipt structure valid, provenance hash verified,
///   journal integrity confirmed. Ready for external STARK verification.
///   This is the maximum verification level achievable on-chain.
///
/// - `PendingExternal`: Basic structure valid but awaiting external ZK
///   verification. Use this when provenance cannot be verified (no gradient).
///
/// - `Invalid`: Verification failed with a specific reason. The proof should
///   be rejected and the node potentially flagged for malicious behavior.
///
/// ## Example Usage
///
/// ```rust,ignore
/// let status = verify_pogq_proof(proof_hash)?;
/// match status.status {
///     VerificationStatus::PartiallyVerified => {
///         // Safe to use for aggregation, but schedule external verification
///         schedule_external_verification(proof_hash);
///     }
///     VerificationStatus::Invalid(reason) => {
///         // Reject the proof
///         flag_node_as_suspicious(status.node_id);
///     }
///     _ => {}
/// }
/// ```
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub enum VerificationStatus {
    /// All checks passed including full STARK proof verification.
    /// **NOT achievable in WASM** - requires external native verifier.
    ///
    /// This status indicates:
    /// - Receipt structure valid
    /// - Provenance hash verified (Blake3)
    /// - Journal integrity confirmed
    /// - Full STARK proof verified by external service
    /// - Method ID (image ID) matches expected zkVM guest
    FullyVerified,

    /// Structure and provenance verified on-chain.
    /// Maximum verification level achievable in WASM.
    ///
    /// This status indicates:
    /// - Receipt structure valid (bincode format, variant, commitments)
    /// - Provenance hash verified against gradient data (Blake3)
    /// - Journal integrity confirmed (quarantine, EMA, rounds, counters)
    /// - Security profile valid (S128 or S192)
    /// - Nonce is non-zero
    ///
    /// **Still requires**: External STARK proof verification
    PartiallyVerified,

    /// Needs off-chain ZK verification by external service.
    /// Basic structure checks passed but provenance not verified.
    ///
    /// This status indicates:
    /// - Receipt structure valid
    /// - Journal integrity confirmed
    /// - Provenance could not be verified (no gradient data provided)
    ///
    /// **Still requires**: Blake3 provenance verification + STARK verification
    PendingExternal,

    /// Failed verification with specific reason.
    /// The proof should be rejected.
    ///
    /// Common failure reasons:
    /// - "Receipt too small/large"
    /// - "Invalid receipt variant"
    /// - "Missing cryptographic commitments"
    /// - "Provenance hash mismatch"
    /// - "Journal integrity failure"
    /// - "Invalid security profile"
    Invalid(String),
}

impl VerificationStatus {
    /// Returns true if the proof is at least partially verified
    pub fn is_usable(&self) -> bool {
        matches!(self, VerificationStatus::FullyVerified | VerificationStatus::PartiallyVerified)
    }

    /// Returns true if the proof needs external verification
    pub fn needs_external(&self) -> bool {
        matches!(self, VerificationStatus::PartiallyVerified | VerificationStatus::PendingExternal)
    }

    /// Returns true if the proof is invalid
    pub fn is_invalid(&self) -> bool {
        matches!(self, VerificationStatus::Invalid(_))
    }

    /// Get the error message if invalid
    pub fn error_message(&self) -> Option<&str> {
        match self {
            VerificationStatus::Invalid(msg) => Some(msg),
            _ => None,
        }
    }
}

