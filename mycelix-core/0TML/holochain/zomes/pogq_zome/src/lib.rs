/// Check if a nonce has been used before
fn is_nonce_used(nonce: &[u8; 32]) -> ExternResult<bool> {
    // Convert nonce to hex string using standard library
    let nonce_hex = nonce.iter()
        .map(|b| format!("{:02x}", b))
        .collect::<String>();

    // Create anchor for nonce registry
    let nonce_registry = anchor(
        LinkTypes::NonceToRegistry,
        "nonce_registry".to_string(),
        nonce_hex,
    )?;

    // Query for existing nonce entries
    let links = get_links(
        GetLinksInputBuilder::try_new(nonce_registry, LinkTypes::NonceToRegistry)?.build()
    )?;

    Ok(!links.is_empty())
}

/// Record a nonce as used
fn record_nonce(nonce_entry: NonceEntry) -> ExternResult<EntryHash> {
    // Create nonce entry
    create_entry(EntryTypes::NonceEntry(nonce_entry.clone()))?;
    let nonce_hash = hash_entry(&nonce_entry)?;

    // Convert nonce to hex string using standard library
    let nonce_hex = nonce_entry.nonce.iter()
        .map(|b| format!("{:02x}", b))
        .collect::<String>();

    // Create anchor for nonce registry
    let nonce_registry = anchor(
        LinkTypes::NonceToRegistry,
        "nonce_registry".to_string(),
        nonce_hex,
    )?;

    // Link nonce to registry
    create_link(
        nonce_registry,
        nonce_hash.clone(),
        LinkTypes::NonceToRegistry,
        (),
    )?;

    Ok(nonce_hash)
}

/// Get the current training round from the DHT
///
/// The current round is determined by:
/// 1. Looking up the round counter anchor
/// 2. Finding all proofs linked to rounds
/// 3. Returning the highest round number
fn get_current_round() -> ExternResult<u64> {
    // Create anchor for round tracker
    let round_tracker = anchor(
        LinkTypes::ProofToRound,
        "round_tracker".to_string(),
        "current".to_string(),
    )?;

    // Query for round info links
    let links = get_links(
        GetLinksInputBuilder::try_new(round_tracker.clone(), LinkTypes::ProofToRound)?.build()
    )?;

    if links.is_empty() {
        // No rounds recorded yet - start at round 0
        return Ok(0);
    }

    // Find the highest round by fetching all round info entries
    let mut max_round: u64 = 0;

    for link in links {
        if let Some(entry_hash) = link.target.into_entry_hash() {
            if let Some(record) = get(entry_hash, GetOptions::default())? {
                // Try to extract round number from entry
                if let Ok(Some(proof)) = record.entry().to_app_option::<PoGQProofEntry>() {
                    max_round = max_round.max(proof.round);
                }
            }
        }
    }

    Ok(max_round)
}

/// Check if a node has already published a proof for a given round
///
/// This prevents nodes from submitting multiple proofs per round,
/// which could be used to manipulate voting weights or consensus.
fn has_node_published_round(node_id: &AgentPubKey, round: u64) -> ExternResult<bool> {
    // Create anchor for node-round tracking
    let node_round_anchor = anchor(
        LinkTypes::ProofToRound,
        "node_rounds".to_string(),
        format!("{}_{}", node_id, round),
    )?;

    // Query for existing entries
    let links = get_links(
        GetLinksInputBuilder::try_new(node_round_anchor, LinkTypes::ProofToRound)?.build()
    )?;

    Ok(!links.is_empty())
}

/// Record that a node has published for a round
///
/// Called after successful proof publication to prevent duplicates.
fn record_node_round(node_id: &AgentPubKey, round: u64, proof_hash: &EntryHash) -> ExternResult<()> {
    // Create anchor for node-round tracking
    let node_round_anchor = anchor(
        LinkTypes::ProofToRound,
        "node_rounds".to_string(),
        format!("{}_{}", node_id, round),
    )?;

    // Link the proof to the node-round anchor
    create_link(
        node_round_anchor,
        proof_hash.clone(),
        LinkTypes::ProofToRound,
        (),
    )?;

    // Also update the round tracker to reflect the new maximum round
    let round_tracker = anchor(
        LinkTypes::ProofToRound,
        "round_tracker".to_string(),
        "current".to_string(),
    )?;

    create_link(
        round_tracker,
        proof_hash.clone(),
        LinkTypes::ProofToRound,
        (),
    )?;

    Ok(())
}

// ============================================================================
// Receipt Verification Module
// ============================================================================
//
// RISC Zero receipts are serialized using bincode. The Receipt struct contains:
// - inner: InnerReceipt (Composite, Succinct, or Fake variants)
// - journal: Journal (public outputs committed by the guest)
// - metadata: ReceiptMetadata (verifier parameters)
//
// Since we cannot deserialize the full Receipt (requires risc0-zkvm),
// we perform structural validation on the raw bytes.

/// RISC Zero receipt verification result
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ReceiptVerificationResult {
    /// Whether structural validation passed
    pub structure_valid: bool,

    /// Whether provenance hash validation passed
    pub provenance_valid: bool,

    /// Detected receipt format version
    pub format_version: Option<u8>,

    /// Estimated journal size (bytes)
    pub journal_size_estimate: Option<usize>,

    /// Whether full ZK verification is required (always true for production)
    pub requires_external_verification: bool,

    /// Detailed error message if validation failed
    pub error: Option<String>,
}

/// Minimum valid receipt size (bincode header + minimal inner receipt)
/// Based on analysis of RISC Zero 1.0+ receipt structure
const MIN_RECEIPT_SIZE: usize = 64;

/// Maximum valid receipt size (10MB - typical proofs are 2-5MB)
const MAX_RECEIPT_SIZE: usize = 10 * 1024 * 1024;

/// Verify RISC Zero receipt structure without full ZK verification
///
/// This performs the maximum verification possible in WASM:
/// 1. Size bounds check
/// 2. Bincode structure validation
/// 3. Receipt variant detection
/// 4. Journal extraction (if possible)
///
/// **NOTE**: This does NOT verify the STARK proof. Full verification
/// requires an external native service running risc0-zkvm.
pub fn verify_receipt_structure(receipt_bytes: &[u8]) -> ReceiptVerificationResult {
    // 1. Size bounds check
    if receipt_bytes.len() < MIN_RECEIPT_SIZE {
        return ReceiptVerificationResult {
            structure_valid: false,
            provenance_valid: false,
            format_version: None,
            journal_size_estimate: None,
            requires_external_verification: true,
            error: Some(format!(
                "Receipt too small: {} bytes (minimum: {})",
                receipt_bytes.len(),
                MIN_RECEIPT_SIZE
            )),
        };
    }

    if receipt_bytes.len() > MAX_RECEIPT_SIZE {
        return ReceiptVerificationResult {
            structure_valid: false,
            provenance_valid: false,
            format_version: None,
            journal_size_estimate: None,
            requires_external_verification: true,
            error: Some(format!(
                "Receipt too large: {} bytes (maximum: {})",
                receipt_bytes.len(),
                MAX_RECEIPT_SIZE
            )),
        };
    }

    // 2. Bincode structure validation
    // Bincode serializes enum variants as u32 index followed by variant data
    // Receipt inner variants: Composite=0, Succinct=1, Fake=2
    let variant_result = validate_bincode_enum_variant(receipt_bytes);

    let format_version = match &variant_result {
        Ok(v) => Some(*v),
        Err(_) => None,
    };

    if variant_result.is_err() {
        return ReceiptVerificationResult {
            structure_valid: false,
            provenance_valid: false,
            format_version,
            journal_size_estimate: None,
            requires_external_verification: true,
            error: Some(variant_result.unwrap_err()),
        };
    }

    // 3. Estimate journal size from receipt structure
    // Journal is typically near the end of the serialized data
    let journal_size = estimate_journal_size(receipt_bytes);

    // 4. Validate receipt contains expected sections
    // Check for presence of cryptographic commitments (32-byte aligned data)
    let has_commitments = validate_commitment_presence(receipt_bytes);

    if !has_commitments {
        return ReceiptVerificationResult {
            structure_valid: false,
            provenance_valid: false,
            format_version,
            journal_size_estimate: journal_size,
            requires_external_verification: true,
            error: Some("Receipt missing expected cryptographic commitments".to_string()),
        };
    }

    ReceiptVerificationResult {
        structure_valid: true,
        provenance_valid: false, // Set by provenance verification
        format_version,
        journal_size_estimate: journal_size,
        requires_external_verification: true,
        error: None,
    }
}

/// Validate bincode enum variant at start of receipt
///
/// Returns the detected variant index (0=Composite, 1=Succinct, 2=Fake)
fn validate_bincode_enum_variant(bytes: &[u8]) -> Result<u8, String> {
    if bytes.len() < 4 {
        return Err("Insufficient bytes for enum variant".to_string());
    }

    // Bincode uses little-endian u32 for enum variants
    let variant = u32::from_le_bytes([bytes[0], bytes[1], bytes[2], bytes[3]]);

    // Valid InnerReceipt variants: 0 (Composite), 1 (Succinct), 2 (Fake)
    match variant {
        0 => Ok(0), // Composite - multi-segment proof
        1 => Ok(1), // Succinct - compressed proof
        2 => {
            // Fake receipts are only valid in dev mode
            // For production, this should be rejected
            #[cfg(debug_assertions)]
            return Ok(2);
            #[cfg(not(debug_assertions))]
            return Err("Fake receipt variant not allowed in release mode".to_string());
        }
        _ => Err(format!("Invalid receipt variant: {}", variant)),
    }
}

/// Estimate journal size from receipt bytes
///
/// The journal is typically serialized near the end of the receipt.
/// This heuristic looks for the journal length prefix.
fn estimate_journal_size(bytes: &[u8]) -> Option<usize> {
    // Journal struct contains:
    // - bytes: Vec<u8> (length-prefixed in bincode)
    //
    // We scan backwards looking for a plausible length prefix
    // that matches remaining data

    if bytes.len() < 12 {
        return None;
    }

    // Check last 1024 bytes for journal-like structure
    let scan_start = if bytes.len() > 1024 {
        bytes.len() - 1024
    } else {
        8
    };

    for offset in (scan_start..bytes.len().saturating_sub(8)).rev() {
        // Read potential u64 length prefix
        let potential_len = u64::from_le_bytes([
            bytes[offset],
            bytes[offset + 1],
            bytes[offset + 2],
            bytes[offset + 3],
            bytes[offset + 4],
            bytes[offset + 5],
            bytes[offset + 6],
            bytes[offset + 7],
        ]) as usize;

        // Check if this could be a journal length
        // Journal typically contains: gradient_hash (32) + epochs (4) + samples (8) = 44+ bytes
        if potential_len >= 32 && potential_len <= 4096 {
            let remaining = bytes.len() - offset - 8;
            if remaining >= potential_len && remaining <= potential_len + 64 {
                return Some(potential_len);
            }
        }
    }

    None
}

/// Validate presence of cryptographic commitments in receipt
///
/// RISC Zero receipts contain multiple 32-byte cryptographic values:
/// - Merkle roots
/// - Polynomial commitments
/// - Digest values
///
/// We check for 32-byte aligned sequences that look like hashes
fn validate_commitment_presence(bytes: &[u8]) -> bool {
    if bytes.len() < 128 {
        return false;
    }

    // Count sequences that look like cryptographic hashes
    // (high entropy, not all zeros or ones)
    let mut hash_like_count = 0;

    for chunk_start in (32..bytes.len().saturating_sub(32)).step_by(32) {
        let chunk = &bytes[chunk_start..chunk_start + 32];

        if is_hash_like(chunk) {
            hash_like_count += 1;
        }

        // Receipts should contain at least 3 hash-like values
        if hash_like_count >= 3 {
            return true;
        }
    }

    hash_like_count >= 1 // Minimal threshold for small proofs
}

/// Check if a 32-byte slice looks like a cryptographic hash
///
/// Heuristics:
/// - Not all zeros
/// - Not all the same byte
/// - Has reasonable byte distribution
fn is_hash_like(bytes: &[u8]) -> bool {
    if bytes.len() != 32 {
        return false;
    }

    // Check not all zeros
    if bytes.iter().all(|&b| b == 0) {
        return false;
    }

    // Check not all same byte
    let first = bytes[0];
    if bytes.iter().all(|&b| b == first) {
        return false;
    }

    // Check has multiple distinct byte values (entropy indicator)
    let mut seen = [false; 256];
    let mut distinct_count = 0;
    for &b in bytes {
        if !seen[b as usize] {
            seen[b as usize] = true;
            distinct_count += 1;
        }
    }

    // Good hashes typically have 15+ distinct bytes in 32 bytes
    distinct_count >= 8
}

// ============================================================================
// Provenance Verification Module (Blake3)
// ============================================================================
//
// Provenance hashes commit to the proof generation environment:
// - Gradient data (Blake3 hash)
// - FL round number
// - Security profile (S128 or S192)
// - Cryptographic nonce
//
// This ensures proofs can only be verified with matching parameters and
// prevents gradient substitution attacks.
//
// ## Blake3 in WASM
//
// The blake3 crate with `default-features = false` compiles to WASM.
// This allows full cryptographic provenance verification on-chain.

/// Compute Blake3 provenance hash for gradient binding
///
/// The provenance hash binds gradient data to proof metadata:
/// ```text
/// Blake3(gradient_data || round_le_bytes || profile_id_le_bytes || nonce)
/// ```
///
/// This provides:
/// - Gradient authenticity (data wasn't modified)
/// - Round binding (can't use gradient from different round)
/// - Profile binding (can't downgrade security profile)
/// - Replay protection (nonce uniqueness)
///
/// Returns [u64; 4] representation of the 256-bit Blake3 hash.
pub fn compute_blake3_provenance(
    gradient_data: &[u8],
    round: u64,
    profile_id: u32,
    nonce: &[u8; 32],
) -> [u64; 4] {
    let mut hasher = blake3::Hasher::new();

    // Hash gradient data
    hasher.update(gradient_data);

    // Hash round (little-endian u64)
    hasher.update(&round.to_le_bytes());

    // Hash profile_id (little-endian u32)
    hasher.update(&profile_id.to_le_bytes());

    // Hash nonce
    hasher.update(nonce);

    // Finalize and convert to [u64; 4]
    let hash = hasher.finalize();
    let hash_bytes = hash.as_bytes();

    [
        u64::from_le_bytes([
            hash_bytes[0], hash_bytes[1], hash_bytes[2], hash_bytes[3],
            hash_bytes[4], hash_bytes[5], hash_bytes[6], hash_bytes[7],
        ]),
        u64::from_le_bytes([
            hash_bytes[8], hash_bytes[9], hash_bytes[10], hash_bytes[11],
            hash_bytes[12], hash_bytes[13], hash_bytes[14], hash_bytes[15],
        ]),
        u64::from_le_bytes([
            hash_bytes[16], hash_bytes[17], hash_bytes[18], hash_bytes[19],
            hash_bytes[20], hash_bytes[21], hash_bytes[22], hash_bytes[23],
        ]),
        u64::from_le_bytes([
            hash_bytes[24], hash_bytes[25], hash_bytes[26], hash_bytes[27],
            hash_bytes[28], hash_bytes[29], hash_bytes[30], hash_bytes[31],
        ]),
    ]
}

/// Compute Blake3 hash of gradient data only
///
/// Returns 32-byte Blake3 hash as Vec<u8>.
/// Use this for gradient_commitment field in GradientEntry.
pub fn compute_gradient_commitment(gradient_data: &[u8]) -> Vec<u8> {
    let hash = blake3::hash(gradient_data);
    hash.as_bytes().to_vec()
}

/// Verify gradient commitment matches expected Blake3 hash
///
/// Returns true if Blake3(gradient_data) == commitment.
pub fn verify_gradient_commitment(gradient_data: &[u8], commitment: &[u8]) -> bool {
    if commitment.len() != 32 {
        return false;
    }

    let computed = blake3::hash(gradient_data);
    computed.as_bytes() == commitment
}

/// Verify provenance hash against expected gradient commitment
///
/// The provenance hash should commit to:
/// 1. Gradient data hash (Blake3)
/// 2. Model parameters hash
/// 3. Training metadata (epochs, samples, learning rate)
///
/// **Implementation**: Now uses Blake3 for full verification in WASM.
pub fn verify_provenance_hash(
    prov_hash: &[u64; 4],
    gradient_commitment: &[u8],
    _profile_id: u32,
) -> Result<bool, String> {
    // Check if provenance is enabled (non-zero)
    let is_zero = prov_hash.iter().all(|&v| v == 0);

    if is_zero {
        // Provenance disabled - valid for dev mode
        // In production, this should require non-zero provenance
        #[cfg(debug_assertions)]
        return Ok(true);

        #[cfg(not(debug_assertions))]
        return Err("Zero provenance hash not allowed in release mode".to_string());
    }

    // Verify gradient commitment is non-empty
    if gradient_commitment.is_empty() {
        return Err("Gradient commitment is empty".to_string());
    }

    // Verify gradient commitment is valid Blake3 hash size (32 bytes)
    if gradient_commitment.len() != 32 {
        return Err(format!(
            "Invalid gradient commitment size: {} (expected 32)",
            gradient_commitment.len()
        ));
    }

    // Basic entropy check on provenance hash
    if prov_hash.iter().all(|&v| v == prov_hash[0]) {
        return Err("Provenance hash lacks entropy".to_string());
    }

    // Check gradient commitment has sufficient entropy
    if gradient_commitment.iter().all(|&b| b == 0) {
        return Err("Gradient commitment is all zeros".to_string());
    }

    // Structural validation passed
    // Full Blake3 verification done by verify_pogq_proof_with_gradient()
    Ok(true)
}

/// Compute a lightweight provenance commitment (for testing)
///
/// **DEPRECATED**: Use `compute_blake3_provenance()` for production.
/// This function is kept for backwards compatibility with tests.
pub fn compute_simple_provenance(
    gradient_commitment: &[u8],
    round: u64,
    profile_id: u32,
    nonce: &[u8; 32],
) -> [u64; 4] {
    // Use Blake3 for consistency with production
    compute_blake3_provenance(gradient_commitment, round, profile_id, nonce)
}

