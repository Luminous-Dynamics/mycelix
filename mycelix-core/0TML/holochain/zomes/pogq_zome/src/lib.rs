/// Marker for proofs that require external ZK verification
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ExternalVerificationRequest {
    /// Entry hash of the proof to verify
    pub proof_hash: EntryHash,

    /// Receipt bytes for external verifier
    pub receipt_bytes: Vec<u8>,

    /// Expected method ID (image ID) - 32 bytes
    pub expected_method_id: Option<Vec<u8>>,

    /// Security profile for verification
    pub profile_id: u32,

    /// Timestamp of request
    pub requested_at: Timestamp,
}

/// Result from external verification service
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ExternalVerificationResponse {
    /// Whether the full ZK proof verified successfully
    pub zk_verified: bool,

    /// Decoded journal contents
    pub journal_data: Option<Vec<u8>>,

    /// Verification timestamp
    pub verified_at: Option<Timestamp>,

    /// Error message if verification failed
    pub error: Option<String>,
}

/// Create external verification request for a proof entry
///
/// This is used to queue proofs for verification by an external
/// native service running risc0-zkvm.
#[hdk_extern]
pub fn create_verification_request(proof_hash: EntryHash) -> ExternResult<ExternalVerificationRequest> {
    let maybe_record = get(proof_hash.clone(), GetOptions::default())?;
    let record = maybe_record
        .ok_or(wasm_error!(WasmErrorInner::Guest("Proof not found".into())))?;

    let entry: PoGQProofEntry = record
        .entry()
        .to_app_option()
        .map_err(|e| wasm_error!(WasmErrorInner::Guest(e.to_string())))?
        .ok_or(wasm_error!(WasmErrorInner::Guest("Invalid entry".into())))?;

    Ok(ExternalVerificationRequest {
        proof_hash,
        receipt_bytes: entry.receipt_bytes,
        expected_method_id: None, // Set by external verifier config
        profile_id: entry.profile_id,
        requested_at: sys_time()?,
    })
}

