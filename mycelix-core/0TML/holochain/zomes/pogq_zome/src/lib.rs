/// Validate PoGQProofEntry before accepting into DHT
///
/// This runs on all nodes that receive the entry, ensuring:
/// 1. Receipt is non-empty and structurally valid
/// 2. Security profile is valid (S128 or S192)
/// 3. Quarantine status is binary
/// 4. Round consistency
/// 5. Nonce is non-zero
pub fn validate_pogq_proof_entry(entry: &PoGQProofEntry) -> Result<(), String> {
    // 1. Receipt validation
    if entry.receipt_bytes.is_empty() {
        return Err("Receipt bytes cannot be empty".to_string());
    }

    if entry.receipt_bytes.len() < MIN_RECEIPT_SIZE {
        return Err(format!(
            "Receipt too small: {} bytes (minimum: {})",
            entry.receipt_bytes.len(),
            MIN_RECEIPT_SIZE
        ));
    }

    if entry.receipt_bytes.len() > MAX_RECEIPT_SIZE {
        return Err(format!(
            "Receipt too large: {} bytes (maximum: {})",
            entry.receipt_bytes.len(),
            MAX_RECEIPT_SIZE
        ));
    }

    // Validate receipt structure (bincode format)
    let receipt_check = verify_receipt_structure(&entry.receipt_bytes);
    if !receipt_check.structure_valid {
        return Err(format!(
            "Invalid receipt structure: {}",
            receipt_check.error.unwrap_or_else(|| "Unknown".to_string())
        ));
    }

    // 2. Security profile validation
    if entry.profile_id != 128 && entry.profile_id != 192 {
        return Err(format!(
            "Invalid security profile: {}. Must be 128 (S128) or 192 (S192)",
            entry.profile_id
        ));
    }

    // 3. Quarantine status validation
    if entry.quarantine_out > 1 {
        return Err(format!(
            "Invalid quarantine status: {}. Must be 0 or 1",
            entry.quarantine_out
        ));
    }

    // 4. Round consistency
    if entry.round != entry.current_round {
        return Err(format!(
            "Round mismatch: round={} != current_round={}",
            entry.round, entry.current_round
        ));
    }

    // 5. Nonce validation
    if entry.nonce == [0u8; 32] {
        return Err("Nonce cannot be all zeros".to_string());
    }

    // 6. EMA validation
    if entry.ema_t_fp > 10 * 65536 {
        return Err(format!(
            "EMA value out of range: {} (max: {})",
            entry.ema_t_fp, 10 * 65536
        ));
    }

    // 7. Consecutive counters mutual exclusion
    if entry.consec_viol_t > 0 && entry.consec_clear_t > 0 {
        return Err(format!(
            "Invalid state: both consec_viol_t ({}) and consec_clear_t ({}) are non-zero",
            entry.consec_viol_t, entry.consec_clear_t
        ));
    }

    // 8. Provenance validation (structural)
    let prov_is_zero = entry.prov_hash == [0, 0, 0, 0];
    if !prov_is_zero {
        // Check entropy
        if entry.prov_hash.iter().all(|&v| v == entry.prov_hash[0]) {
            return Err("Provenance hash lacks entropy".to_string());
        }

        // AIR revision must be non-zero with provenance enabled
        if entry.air_rev == 0 {
            return Err("AIR revision cannot be zero with enabled provenance".to_string());
        }
    }

    // In release mode, provenance must be enabled
    #[cfg(not(debug_assertions))]
    if prov_is_zero {
        return Err("Zero provenance hash not allowed in production".to_string());
    }

    Ok(())
}

/// Validate GradientEntry before accepting into DHT
pub fn validate_gradient_entry(entry: &GradientEntry) -> Result<(), String> {
    // Gradient commitment must be exactly 32 bytes (Blake3 hash)
    if entry.gradient_commitment.len() != 32 {
        return Err(format!(
            "Invalid gradient commitment size: {} (expected 32 bytes)",
            entry.gradient_commitment.len()
        ));
    }

    // Nonce cannot be all zeros
    if entry.nonce == [0u8; 32] {
        return Err("Gradient nonce cannot be all zeros".to_string());
    }

    // Quality score must be in valid range [0.0, 1.0]
    if entry.quality_score < 0.0 || entry.quality_score > 1.0 {
        return Err(format!(
            "Quality score out of range: {} (must be 0.0-1.0)",
            entry.quality_score
        ));
    }

    Ok(())
}

/// Validate NonceEntry before accepting into DHT
pub fn validate_nonce_entry(entry: &NonceEntry) -> Result<(), String> {
    // Nonce cannot be all zeros
    if entry.nonce == [0u8; 32] {
        return Err("Nonce cannot be all zeros".to_string());
    }

    Ok(())
}

