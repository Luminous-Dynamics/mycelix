//! PoGQ Zome - Byzantine Detection with RISC Zero Proofs
//!
//! This zome integrates RISC Zero zkVM proofs for Proof-of-Gradient-Quality (PoGQ)
//! Byzantine detection in federated learning. It provides:
//!
//! 1. **Proof Publishing**: Nodes publish RISC Zero receipts proving correct PoGQ state transitions
//! 2. **Nonce Binding**: Cryptographic binding between proofs and gradients prevents replay attacks
//! 3. **Verification**: Receipt structure + provenance validation (full ZK verification external)
//! 4. **Sybil Weighting**: Quarantined nodes receive zero weight in aggregation
//!
//! **Architecture**: Adapted from Winterfell design for RISC Zero primary backend
//! **Security**: 127-bit (S128) or 191-bit (S192) conjectured security
//! **Provenance**: Blake3 hash commitment over build metadata
//!
//! ## Verification Strategy
//!
//! RISC Zero zkVM cannot compile to WASM (native dependencies). This zome implements:
//! - **Receipt Structure Verification**: Validates bincode-serialized Receipt format
//! - **Provenance Hash Verification**: Blake3 hash of gradient commitments
//! - **Journal Integrity**: Validates decision outputs (quarantine, EMA, rounds)
//! - **External ZK Verification**: Full STARK proof verification delegated to native service
//!
//! ## On-Chain vs Off-Chain Verification
//!
//! ### Verified On-Chain (in WASM)
//!
//! The following verifications are performed directly in this zome:
//!
//! 1. **Receipt Structure Validation**
//!    - Size bounds checking (MIN_RECEIPT_SIZE to MAX_RECEIPT_SIZE)
//!    - Bincode enum variant validation (Composite=0, Succinct=1, Fake=2)
//!    - Cryptographic commitment presence detection
//!    - Journal size estimation
//!
//! 2. **Blake3 Provenance Hash Verification**
//!    - Computes Blake3 hash of gradient data
//!    - Verifies hash matches the provenance claim in the proof entry
//!    - Ensures gradient-to-proof binding integrity
//!
//! 3. **Journal Integrity Validation**
//!    - Quarantine status is binary (0 or 1)
//!    - Round consistency (entry.round == journal.current_round)
//!    - EMA value within bounds (Q16.16 fixed-point)
//!    - Consecutive counter mutual exclusion
//!
//! 4. **Security Profile Validation**
//!    - Profile must be S128 (128) or S192 (192)
//!    - AIR revision non-zero when provenance enabled
//!
//! 5. **Nonce Validation**
//!    - Non-zero nonce requirement
//!    - Replay attack prevention via nonce registry
//!
//! ### Requires Off-Chain Verification (External Service)
//!
//! The following CANNOT be verified in WASM due to RISC Zero's native dependencies:
//!
//! 1. **Full STARK Proof Verification**
//!    - FRI polynomial commitment verification
//!    - Merkle proof validation
//!    - Constraint system satisfaction
//!
//! 2. **Method ID (Image ID) Verification**
//!    - Verify receipt was generated by expected zkVM guest program
//!    - Requires risc0-zkvm native library
//!
//! 3. **Journal Decoding**
//!    - Full deserialization of journal contents
//!    - Type-safe extraction of DecisionJournal fields
//!
//! ### Verification Flow
//!
//! ```text
//! Client                    PoGQ Zome                    External Verifier
//!   |                          |                              |
//!   |-- publish_pogq_proof --->|                              |
//!   |                          |-- validate structure         |
//!   |                          |-- check nonce freshness      |
//!   |                          |-- store in DHT               |
//!   |<-- EntryHash ------------|                              |
//!   |                          |                              |
//!   |-- publish_gradient ----->|                              |
//!   |                          |-- verify nonce binding       |
//!   |                          |-- Blake3 provenance check    |
//!   |<-- EntryHash ------------|                              |
//!   |                          |                              |
//!   |-- verify_pogq_proof ---->|                              |
//!   |                          |-- receipt structure check    |
//!   |                          |-- provenance verification    |
//!   |                          |-- journal integrity check    |
//!   |<-- VerificationStatus ---|                              |
//!   |    (PartiallyVerified)   |                              |
//!   |                          |                              |
//!   |-- request_external_verification ----------------------->|
//!   |                          |                              |-- full STARK verify
//!   |                          |                              |-- method ID check
//!   |<-- ExternalVerificationResponse ------------------------|
//!   |    (FullyVerified or Invalid)                           |
//! ```
//!
//! ## Entry Types
//!
//! - `PoGQProofEntry`: RISC Zero receipt + provenance + decision outputs
//! - `GradientEntry`: Gradient metadata linked to proof via nonce
//!
//! ## Zome Functions
//!
//! - `publish_pogq_proof`: Validate and publish RISC Zero receipt
//! - `verify_pogq_proof`: Receipt structure + journal verification (returns VerificationStatus)
//! - `verify_pogq_proof_with_gradient`: Full on-chain verification including Blake3 provenance
//! - `publish_gradient`: Bind gradient to proof (nonce validation)
//! - `get_round_gradients`: Fetch gradients with quarantine status
//! - `compute_sybil_weighted_aggregate`: Weight gradients by quarantine state

