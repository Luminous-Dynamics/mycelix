"""Generate derived statistics for the Zero-TrustML paper.

The script computes mean and standard deviation values for Mode 1 results
and emits LaTeX macros consumed by the main manuscript. Keep the source
values here in sync with the experimental harness.
"""

from __future__ import annotations

import math
from pathlib import Path


RESULTS = {
    20: {"detection": [100.0], "fpr": [0.0]},
    25: {"detection": [100.0], "fpr": [0.0]},
    30: {"detection": [100.0], "fpr": [0.0]},
    35: {"detection": [100.0], "fpr": [7.7]},
    40: {"detection": [100.0], "fpr": [8.3]},
    45: {"detection": [100.0, 100.0, 100.0], "fpr": [0.0, 7.7, 9.1]},
    50: {"detection": [100.0], "fpr": [10.0]},
}


def mean_std(values: list[float]) -> tuple[float, float]:
    mean = sum(values) / len(values)
    if len(values) == 1:
        return round(mean, 1), 0.0
    variance = sum((v - mean) ** 2 for v in values) / len(values)
    std = math.sqrt(variance)
    return round(mean, 1), round(std, 1)


def generate_macros(output: Path) -> None:
    lines = ["% Auto-generated by build_results.py", ""]

    for bft, metrics in sorted(RESULTS.items()):
        det_mean, det_std = mean_std(metrics["detection"])
        fpr_mean, fpr_std = mean_std(metrics["fpr"])
        lines.append(f"\\newcommand{{\\ModeOneDetectionBft{bft}Mean}}{{{det_mean}}}")
        lines.append(f"\\newcommand{{\\ModeOneDetectionBft{bft}Std}}{{{det_std}}}")
        lines.append(f"\\newcommand{{\\ModeOneFprBft{bft}Mean}}{{{fpr_mean}}}")
        lines.append(f"\\newcommand{{\\ModeOneFprBft{bft}Std}}{{{fpr_std}}}")
        lines.append("")

    output.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    base = Path(__file__).resolve().parents[1]
    generated_dir = base / "generated"
    generated_dir.mkdir(parents=True, exist_ok=True)

    macros_path = generated_dir / "stats.tex"
    generate_macros(macros_path)


if __name__ == "__main__":
    main()
